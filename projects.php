	<article>
		<h1>Decision Helping Expert System for Erasmus students</h1>
		<p>
			This Expert System is a prototype of a computerizing tool. It was created to help the Outgoing Department of the <em>Université de Technologie de Compiègne</em> in the whole student selecting process for a semester of studies abroad.
		</p>
		<p class ="articleimg">
			<a href="./img/ri021.png" rel="lightbox"><img src="./img/ri02min1.png" alt="screenshot expert system" /></a>
			<a href="./img/ri022.png" rel="lightbox"><img src="./img/ri02min2.png" alt="screenshot expert system" /></a>
		</p>
		<p>
			The system, written in Common Lisp, is based on two inference engines.
		</p>
		<p>
			The first one uses forward-chaining. It starts the deducing process by the goal : a matching student profile for a semester in another university. It then asks questions based on what can be helpful or not for the student to go to the university.
		</p>
		<p>
			The second one, backward-chaing based, starts by the student's profile and asks dynamically the good questions to try and deducing his affinity with the university.
		</p>
		<p>
			Although the code is a bit dense, you can take a look at it <a href="https://github.com/amasciul/RI02-expert">here</a>.
		</p>
	</article>
